
/* Open the sidenav */
function openNav() {
  document.getElementById("mySidenav").style.width = "100%";
  document.getElementById("myHamburger").style.opacity = "0"; 
}

/* Close/hide the sidenav */
function closeNav() {
  document.getElementById("mySidenav").style.width = "0";
  document.getElementById("myHamburger").style.opacity = "1"; 
}
